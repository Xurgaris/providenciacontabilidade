const state = {
  category: "Todos",
  search: ""
};

const blogGrid = document.getElementById("blogGrid");
const featuredPost = document.getElementById("featuredPost");
const categoriesContainer = document.getElementById("blogCategories");
const searchInput = document.getElementById("blogSearch");
const emptyState = document.getElementById("emptyState");

const postModal = document.getElementById("postModal");
const modalImage = document.getElementById("modalImage");
const modalMeta = document.getElementById("modalMeta");
const modalTitle = document.getElementById("modalTitle");
const modalExcerpt = document.getElementById("modalExcerpt");
const modalContent = document.getElementById("modalContent");

function formatDate(dateString) {
  const date = new Date(dateString);

  return date.toLocaleDateString("pt-BR", {
    day: "2-digit",
    month: "long",
    year: "numeric"
  });
}

function formatTime(dateString) {
  const date = new Date(dateString);

  return date.toLocaleTimeString("pt-BR", {
    hour: "2-digit",
    minute: "2-digit"
  });
}

function getSortedPosts() {
  return [...blogPosts].sort((a, b) => {
    return new Date(b.publishedAt) - new Date(a.publishedAt);
  });
}

function getFilteredPosts() {
  const search = state.search.trim().toLowerCase();

  return getSortedPosts().filter((post) => {
    const matchesCategory =
      state.category === "Todos" || post.category === state.category;

    const searchableText = `
      ${post.title}
      ${post.excerpt}
      ${post.category}
      ${post.author}
    `.toLowerCase();

    const matchesSearch = searchableText.includes(search);

    return matchesCategory && matchesSearch;
  });
}

function createMetaHTML(post) {
  return `
    <span class="post-category">${post.category}</span>
    <span>${formatDate(post.publishedAt)}</span>
    <span>${formatTime(post.publishedAt)}</span>
    <span>${post.readTime} de leitura</span>
  `;
}

function renderCategories() {
  if (!categoriesContainer) return;

  const categories = ["Todos", ...new Set(blogPosts.map((post) => post.category))];

  categoriesContainer.innerHTML = categories
    .map((category) => {
      const isActive = category === state.category ? "active" : "";

      return `
        <button class="category-chip ${isActive}" data-category="${category}">
          ${category}
        </button>
      `;
    })
    .join("");
}

function renderFeaturedPost() {
  if (!featuredPost) return;

  const posts = getFilteredPosts();
  const post = posts[0];

  if (!post) {
    featuredPost.innerHTML = "";
    return;
  }

  featuredPost.innerHTML = `
    <div 
      class="featured-card__image" 
      style="background-image: linear-gradient(135deg, rgba(5, 5, 5, 0.08), rgba(5, 5, 5, 0.72)), url('${post.image}');"
    ></div>

    <div class="featured-card__content">
      <div class="post-meta">
        ${createMetaHTML(post)}
      </div>

      <h3>${post.title}</h3>

      <p>${post.excerpt}</p>

      <button class="read-more" data-open-post="${post.slug}">
        Ler artigo completo
        <span>→</span>
      </button>
    </div>
  `;
}

function renderPosts() {
  if (!blogGrid) return;

  const posts = getFilteredPosts();

  blogGrid.innerHTML = posts
    .map((post) => {
      return `
        <article class="post-card">
          <div 
            class="post-card__image" 
            style="background-image: linear-gradient(135deg, rgba(5, 5, 5, 0.10), rgba(5, 5, 5, 0.68)), url('${post.image}');"
          ></div>

          <div class="post-card__content">
            <div class="post-meta">
              ${createMetaHTML(post)}
            </div>

            <h3>${post.title}</h3>

            <p>${post.excerpt}</p>

            <button class="read-more" data-open-post="${post.slug}">
              Ver conteúdo
              <span>→</span>
            </button>
          </div>
        </article>
      `;
    })
    .join("");

  if (emptyState) {
    emptyState.classList.toggle("active", posts.length === 0);
  }
}

function createContentHTML(content) {
  return content
    .map((block) => {
      if (block.type === "h3") {
        return `<h3>${block.text}</h3>`;
      }

      if (block.type === "ul") {
        return `
          <ul>
            ${block.items.map((item) => `<li>${item}</li>`).join("")}
          </ul>
        `;
      }

      return `<p>${block.text}</p>`;
    })
    .join("");
}

function openPost(slug) {
  const post = blogPosts.find((item) => item.slug === slug);

  if (!post || !postModal) return;

  if (modalImage) {
    modalImage.style.backgroundImage = `
      linear-gradient(180deg, rgba(8, 13, 23, 0.08), #080d17),
      url('${post.image}')
    `;
  }

  if (modalMeta) modalMeta.innerHTML = createMetaHTML(post);
  if (modalTitle) modalTitle.textContent = post.title;
  if (modalExcerpt) modalExcerpt.textContent = post.excerpt;
  if (modalContent) modalContent.innerHTML = createContentHTML(post.content);

  postModal.classList.add("active");
  postModal.setAttribute("aria-hidden", "false");
  document.body.classList.add("modal-open");
}

function closePost() {
  if (!postModal) return;

  postModal.classList.remove("active");
  postModal.setAttribute("aria-hidden", "true");
  document.body.classList.remove("modal-open");
}

function bindEvents() {
  if (categoriesContainer) {
    categoriesContainer.addEventListener("click", (event) => {
      const button = event.target.closest("[data-category]");

      if (!button) return;

      state.category = button.dataset.category;

      renderCategories();
      renderFeaturedPost();
      renderPosts();
    });
  }

  if (searchInput) {
    searchInput.addEventListener("input", (event) => {
      state.search = event.target.value;

      renderFeaturedPost();
      renderPosts();
    });
  }

  document.addEventListener("click", (event) => {
    const openButton = event.target.closest("[data-open-post]");
    const closeButton = event.target.closest("[data-close-modal]");

    if (openButton) {
      openPost(openButton.dataset.openPost);
    }

    if (closeButton) {
      closePost();
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
      closePost();
    }
  });
}

function initBlog() {
  renderCategories();
  renderFeaturedPost();
  renderPosts();
  bindEvents();
}

document.addEventListener("DOMContentLoaded", initBlog);